#include <iostream>
using namespace std;
int main() {
    // for loop - print 1 to 10
    for (int i = 1; i <= 10; i++) {
        cout << i << endl;
    }
    // while loop - print even numbers from 2 to 20
    int k = 2;
    while (k <= 20) {
        cout << k << endl;
        k += 2;
    }
    return 0;
}