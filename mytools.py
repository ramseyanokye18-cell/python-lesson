def addition(number1,number2):
    return number1 + number2
def substraction(number1,number2):
    return number1 - number2
def multiplication(number1,number2):
    return number1 * number2
def division(number1,number2):
    return number1 / number2
def square(number):
    return number**2
# is_even
def is_even(number):
    if number % 2 == 0:
        return True
    else:
        return False
    
